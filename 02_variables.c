#include <stdio.h>

int main()
{

    int a = 44;     // int for integers
    float b = 9.74; // float for floating point numbers
    char c = 'O'; // char for character
    int num_one = 10;
    int num_two = 5;
    printf("The value of a is %d \n", a); // %d for integer
    printf("The value of b is %f \n", b); // %f for floating point number
    printf("The value of c is %c \n", c); // %c for character
    printf("Addition of two numbers is %d \n", num_one + num_two);
    printf("Subtraction of two numbers is %d \n", num_one - num_two);
    printf("Multiplication of two numbers is %d \n", num_one * num_two);
    printf("Division of two numbers is %d \n", num_one / num_two);
    return 0;
}

